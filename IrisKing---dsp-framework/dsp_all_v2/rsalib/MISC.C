void test_rsa()
{
	int i, j, len;
	char buf[256];
	char encrypt[256];
	char decrypt[256];
	int len1 = 48;
	struct RSAKey *key;
	key = safemalloc(1, sizeof(struct RSAKey));
	
	dbg_output("start\n");

	build_public_key(public_key, key);
	build_private_key(private_key, key);

	for (len1 = 110; len1 < 128; len1 ++)
	{
		for (j = 0; j < 10; j++)
		{
			for (i = 0; i< 256; i++)
			{
				buf[i] = random_byte();
			}

			dbg_output("len %d ", len1);

			if (!(len = rsa_encrypt_public(buf, len1, encrypt, 256, key)))
			{
				dbg_output("encrypt failed\n");
				continue;
			}

			if (!(len = rsa_decrypt_private(encrypt, len, decrypt, 256, key)))
			{
				dbg_output("decrypt failed\n");
				continue;
			}
			
			if (memcmp(decrypt, buf, len1) == 0)
				dbg_output("equal\n");
			else
				dbg_output("not equal\n");
		}
	}
	freersakey(key);
	safefree(key);
}
