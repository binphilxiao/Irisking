int random_byte(void)
{
	return rand();
}

void dump_char(unsigned char c)
{
	dbg_output( "0x%02x", c);
}

int dump_int(unsigned int n)
{
	int i;
	unsigned char* p = (unsigned char *)&n;
	for (i = 0; i < 4; i++)
	{
		dump_char(p[i]);
		dbg_output( ", ");
	}
	return 0;
}

int dump_bignum(Bignum bn)
{
	int len, i, j;
	char *p;
	len = ((bignum_bitcount(bn) + 7) >> 3);
	p = malloc(len);
	dump_int(len);
	dbg_output( "\n");

	j = 1;
	for (i = len - 1; i >= 0; i--, j++)
	{
		dump_char(bignum_byte(bn, i) ^ 0xaa);
		if (i)
			dbg_output( ", ");
		if ((j % 8 == 0) && i)
			dbg_output( "\n");
	}
	return 0;
}

