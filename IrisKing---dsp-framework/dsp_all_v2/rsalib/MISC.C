void *safemalloc(size_t n, size_t size)
{
    void *p;

    if (n > INT_MAX / size) {
		p = NULL;
    } else {
		size *= n;
		if (size == 0) size = 1;
		p = malloc(size);
    }

    if (!p) {
		while(1);
    }
    return p;
}

void *saferealloc(void *ptr, size_t n, size_t size)
{
    void *p;

    if (n > INT_MAX / size) {
	p = NULL;
    } else {
		size *= n;
		if (!ptr) {
			p = malloc(size);
		} else {
			p = realloc(ptr, size);
		}
    }

    if (!p) {
		while(1);
    }
    return p;
}

void safefree(void *ptr)
{
    if (ptr) {
		free(ptr);
    }
}

